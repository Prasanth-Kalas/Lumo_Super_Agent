create or replace function public.tg_handle_new_profile_budget_tier()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.user_budget_tiers (user_id)
  values (new.id)
  on conflict (user_id) do nothing;
  return new;
end;
$$;

