-- Per-invocation cost ledger. One row per request_id; retries reconcile the
-- same row rather than appending attempts. Identity columns are immutable.
create table if not exists public.agent_cost_log (
  id                         bigint generated by default as identity primary key,
  request_id                 uuid not null,
  user_id                    uuid not null references public.profiles(id) on delete cascade,
  agent_id                   text not null,
  agent_version              text not null,
  capability_id              text,
  mission_id                 uuid references public.missions(id) on delete set null,
  mission_step_id            uuid references public.mission_steps(id) on delete set null,
  model_used                 text,
  prompt_tokens              integer not null default 0 check (prompt_tokens >= 0),
  completion_tokens          integer not null default 0 check (completion_tokens >= 0),
  brain_calls_usd            numeric(12,6) not null default 0 check (brain_calls_usd >= 0),
  brain_call_count           integer not null default 0 check (brain_call_count >= 0),
  model_tokens_input         integer not null default 0 check (model_tokens_input >= 0),
  model_tokens_output        integer not null default 0 check (model_tokens_output >= 0),
  model_tokens_cost_usd      numeric(12,6) not null default 0 check (model_tokens_cost_usd >= 0),
  connector_calls            integer not null default 0 check (connector_calls >= 0),
  connector_calls_usd        numeric(12,6) not null default 0 check (connector_calls_usd >= 0),
  cost_usd_total             numeric(12,6) not null default 0 check (cost_usd_total >= 0),
  cost_usd_platform          numeric(12,6) not null default 0 check (cost_usd_platform >= 0),
  cost_usd_developer_share   numeric(12,6) not null default 0 check (cost_usd_developer_share >= 0),
  total_usd                  numeric(12,6) not null default 0 check (total_usd >= 0),
  status                     text not null default 'completed'
    check (status in ('completed','aborted_budget','aborted_error')),
  evidence                   jsonb not null default '{}'::jsonb,
  created_at                 timestamptz not null default now(),
  updated_at                 timestamptz not null default now(),
  unique (request_id),
  check (cost_usd_total >= cost_usd_platform + cost_usd_developer_share),
  check (total_usd >= brain_calls_usd + model_tokens_cost_usd + connector_calls_usd)
);

