-- Idempotency table for daily/monthly digest crons.
create table if not exists public.user_cost_digest_deliveries (
  user_id             uuid not null references public.profiles(id) on delete cascade,
  digest_type         text not null check (digest_type in ('daily','monthly')),
  period_start        date not null,
  period_end          date not null,
  spend_usd           numeric(12,6) not null default 0 check (spend_usd >= 0),
  delivery_state      text not null default 'pending'
    check (delivery_state in ('pending','sent','skipped','failed')),
  notification_id     uuid,
