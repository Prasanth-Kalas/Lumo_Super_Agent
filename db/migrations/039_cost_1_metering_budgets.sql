-- Per-user tier/cap configuration. Scope-specific caps remain in
-- agent_scope_grants.constraints from PERM-1; this table is the user-wide
-- budget layer from ADR-016.
create table if not exists public.user_budget_tiers (
  user_id                  uuid primary key references public.profiles(id) on delete cascade,
  tier                     text not null default 'free'
    check (tier in ('free','pro','enterprise')),
  daily_cap_usd            numeric(10,2) not null default 0.50 check (daily_cap_usd >= 0),
  monthly_cap_usd          numeric(10,2) not null default 5.00 check (monthly_cap_usd >= 0),
  daily_cap_override_usd   numeric(10,2) check (daily_cap_override_usd is null or daily_cap_override_usd >= 0),
  monthly_cap_override_usd numeric(10,2) check (monthly_cap_override_usd is null or monthly_cap_override_usd >= 0),
  soft_cap                 boolean not null default false,
  effective_from           timestamptz not null default now(),
  effective_until          timestamptz,
  notes                    text,
  created_at               timestamptz not null default now(),
  updated_at               timestamptz not null default now(),
  check (effective_until is null or effective_until > effective_from)
);

comment on table public.user_budget_tiers is
  'COST-1 per-user daily/monthly budget tier. Enterprise can use soft caps and override columns.';

