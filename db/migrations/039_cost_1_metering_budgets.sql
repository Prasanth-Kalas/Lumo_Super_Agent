  after insert on public.profiles
  for each row execute function public.tg_handle_new_profile_budget_tier();

insert into public.user_budget_tiers (user_id)
  select p.id
  from public.profiles p
  left join public.user_budget_tiers ubt on ubt.user_id = p.id
  where ubt.user_id is null;

-- Rollups are materialized by COST-1 crons so dispatch checks don't scan the
-- full cost ledger on every invocation.
create table if not exists public.user_cost_rollups_daily (
  user_id                  uuid not null references public.profiles(id) on delete cascade,
  local_date               date not null,
  window_start_at          timestamptz not null,
  window_end_at            timestamptz not null,
  invocation_count         integer not null default 0 check (invocation_count >= 0),
  aborted_budget_count     integer not null default 0 check (aborted_budget_count >= 0),
  aborted_error_count      integer not null default 0 check (aborted_error_count >= 0),
  fallback_count           integer not null default 0 check (fallback_count >= 0),
  prompt_tokens            integer not null default 0 check (prompt_tokens >= 0),
  completion_tokens        integer not null default 0 check (completion_tokens >= 0),
  cost_usd_total           numeric(12,6) not null default 0 check (cost_usd_total >= 0),
  cost_usd_platform        numeric(12,6) not null default 0 check (cost_usd_platform >= 0),
  cost_usd_developer_share numeric(12,6) not null default 0 check (cost_usd_developer_share >= 0),
  top_agents               jsonb not null default '[]'::jsonb,
  capability_breakdown     jsonb not null default '{}'::jsonb,
  created_at               timestamptz not null default now(),
  updated_at               timestamptz not null default now(),
  primary key (user_id, local_date),
  check (window_end_at > window_start_at),
  check (cost_usd_total >= cost_usd_platform + cost_usd_developer_share)
);

