create or replace function public.agent_cost_log_retry_safe_append_only()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  if (tg_op = 'UPDATE') then
    if old.request_id is distinct from new.request_id
       or old.user_id is distinct from new.user_id
       or old.agent_id is distinct from new.agent_id
       or old.agent_version is distinct from new.agent_version
       or old.capability_id is distinct from new.capability_id
       or old.mission_id is distinct from new.mission_id
       or old.mission_step_id is distinct from new.mission_step_id
       or old.created_at is distinct from new.created_at then
      raise exception 'AGENT_COST_LOG_IDENTITY_IMMUTABLE'
        using hint = 'Retries may reconcile cost/status for the same request_id, but invocation identity fields are immutable';
    end if;
    return new;
  end if;

  if (tg_op = 'DELETE') then
    if current_setting('lumo.allow_agent_cost_log_delete', true) <> 'true' then
      raise exception 'AGENT_COST_LOG_APPEND_ONLY'
        using hint = 'agent_cost_log is append-only except account-deletion cascade';
    end if;
    return old;
  end if;

  return null;
end;
$$;

